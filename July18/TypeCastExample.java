public class TypeCastExample {
	public static void main(String [] args) {
		byte b = 2;
		short s = 3;
		s = (short) (s + b);
		
		System.out.println(s);
		
		int num = 1;
		double cost = 2.0;
		System.out.println(cost + num);		
	}
}